package Javapractice;

public class Conditional {

	public static void main(String[] args) {
		int value1 = 45;
		int value2 = 56;
			if(value1 == value2)
				System.out.println("value1 == value2");
			if(value1 != value2)
				System.out.println("value1 != value2");
			if(value1 > value2)
				System.out.println("value1 > value2");
			if(value1 < value2)
				System.out.println("value1 < value2");
			if(value1 <= value2)
				System.out.println("value1 <= value2");
	}
}